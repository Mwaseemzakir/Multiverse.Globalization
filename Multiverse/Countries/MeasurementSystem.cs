namespace Multiverse.Globalization.Countries;

/// <summary>
/// Specifies the measurement system used in a country.
/// </summary>
public enum MeasurementSystem
{
    /// <summary>International System of Units (SI) — used by most countries.</summary>
    Metric,

    /// <summary>Imperial system — used by the United Kingdom for some measurements.</summary>
    Imperial,

    /// <summary>United States customary units — used by the United States.</summary>
    USCustomary
}
