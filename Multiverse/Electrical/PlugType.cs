namespace Multiverse.Globalization.Electrical;

/// <summary>
/// Represents electrical plug/socket types used worldwide (IEC 60083 designations).
/// </summary>
public enum PlugType
{
    /// <summary>Type A — Two flat parallel pins (North America, Japan).</summary>
    A,
    /// <summary>Type B — Two flat parallel pins + grounding pin (North America).</summary>
    B,
    /// <summary>Type C — Two round pins (Europe, South America, Asia).</summary>
    C,
    /// <summary>Type D — Three large round pins in triangle (India, Nepal).</summary>
    D,
    /// <summary>Type E — Two round pins + grounding pin hole (France, Belgium, Poland).</summary>
    E,
    /// <summary>Type F — Two round pins + grounding clips (Germany, Europe).</summary>
    F,
    /// <summary>Type G — Three rectangular pins (United Kingdom, Ireland, Malaysia).</summary>
    G,
    /// <summary>Type H — Three pins in V-shape (Israel).</summary>
    H,
    /// <summary>Type I — Two angled flat pins + grounding (Australia, China, Argentina).</summary>
    I,
    /// <summary>Type J — Three round pins (Switzerland, Liechtenstein).</summary>
    J,
    /// <summary>Type K — Three round pins (Denmark, Greenland).</summary>
    K,
    /// <summary>Type L — Three round pins in a row (Italy, Chile).</summary>
    L,
    /// <summary>Type M — Three large round pins (South Africa, India for heavy appliances).</summary>
    M,
    /// <summary>Type N — Three round pins (Brazil, South Africa).</summary>
    N,
    /// <summary>Type O — Three round pins (Thailand).</summary>
    O
}
