namespace Multiverse.Globalization.Languages;

/// <summary>
/// Specifies the text writing direction of a language.
/// </summary>
public enum TextDirection
{
    /// <summary>Text is written from left to right (e.g. English, French, German).</summary>
    LeftToRight,

    /// <summary>Text is written from right to left (e.g. Arabic, Hebrew, Urdu).</summary>
    RightToLeft
}
